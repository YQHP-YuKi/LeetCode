package Medium.Remove_Nth_Node_From_End_of_List_19;

/**
 * @author YQHP-YuKi
 * @create 2021-10-25 8:45
 */
public class ListNode {

  int val;
  ListNode next;

  ListNode() {
  }

  ListNode(int val) {
    this.val = val;
  }

  ListNode(int val, ListNode next) {
    this.val = val;
    this.next = next;
  }
}
